package Operators;
// programe for Relational operators
public class RelationalOperators {
	public static void main(String args[])
	{
		int a = 10;
		int b = 30;
		
		System.out.println("a>b= "+ (a>b));    //10 is less then 20. ans is false
		System.out.println("a<b= "+ (a<b));
		System.out.println("a<=b= "+ (a<=b));
		System.out.println("a>=b= "+ (a>=b));
		System.out.println("a!=b= "+ (a!=b));
	}

}
